package lessons;

public class Switch {

	public static void main(String[] args) {

		int choice = 1;
		
		switch (choice)  {
		
		case 1:
			System.out.println("Case 1 matched.");
			break;
			
		case 2:
			System.out.println("Case 2 matched.");
			break;
			
		case 3:
			System.out.println("Case 3 matched.");
			break;
		
		default:
			System.out.println("Default matched.");
		
		}
		

	}

}
