package lessons;

public class Factorial {

	public static void main(String[] args) {
		
		System.out.println(factorial(-9));
	}
	
	/*	DESCRIPTION - Returns factorial of n.
	 * 	PARAMETERS - int n
	 * 	RETURN TYPE - int
	 */
	public static int factorial(int n)  {
		
		// error case
		if (n < 0)
			return -1;
		
		// when user enters 1
		if (n == 0)
			return 1;
		
		
		
		
		return 99;
	}

}
