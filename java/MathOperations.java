package Lessons;

public class MathOperations {

	public static void main(String[] args) {

		// variables
		int sum = 0;
		int difference = 0;
		int product = 0;
		double quotient = 0.0;
		int modulus = 0;
		
		// Math operators: +, -, *, /, %
		
		// Addition
		sum = 1 + 2;
		System.out.println(sum);
		
		// Subtraction
		difference = 13 - 7;
		System.out.println(difference);
		
		// Multiplication
		product = 5 * 3;
		System.out.println(product);
		
		// Division
		quotient = 7.0 / 2.0;
		System.out.println(quotient);
		
		// Modulus
		modulus = 10 % 6;
		System.out.println(modulus);
		
	}

}
