# Math Operations

# Addition
num1 = 3
num2 = 4
answer = num1 + num2
print(answer)

# Subtraction
print(5-2)

# Multiplication
print(3*3)

# Division (always returns float)
print(4/2)

# Integer division returns integer, does not round answer)
print(5//2)

# Modulus (gives remainder)
print(5%2)

# Exponentiation
print(2**3)