import math
import random

# returns greatest common factor of a and b
print(math.gcd(8, 4))

# returns the ceiling of x
print(math.ceil(3.0))

# returns the floor of x
print(math.floor(4.9))

# random number between x and y
print(random.randint( 1, 6))

# random decimal less than 1
print(random.random())
